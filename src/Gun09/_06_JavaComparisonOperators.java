package Gun09;

public class _06_JavaComparisonOperators {
    public static void main(String[] args) {
        int a=50;
        int b=50;

        System.out.println("a b ye eşit mi = " + (a==b));
        System.out.println("a b den farklı mı = " + (a!=b));

        System.out.println("a b den buyuk mü = " + (a>b));
        System.out.println("a b den kucuk mü = " + (a<b));

        System.out.println("a b den buyuk eşit mi = " + (a>=b));
        System.out.println("a b den kucuk eşit mi = " + (a<=b));
    }
}
