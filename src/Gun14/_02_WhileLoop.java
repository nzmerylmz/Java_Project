package Gun14;

public class _02_WhileLoop {
    public static void main(String[] args) {
        int sayac = 0;


        //sayac 5 den küçük iken(while)
        while (sayac < 5) { //şart sağlanıyorsa parantezlerin arasını çalıştır
            sayac++;
            System.out.println("Merhaba"+sayac);

        }
    }
}
