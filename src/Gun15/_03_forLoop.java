package Gun15;

public class _03_forLoop {
    public static void main(String[] args) {
        for (int i=0; i<=10;i++) {
            System.out.println(i);
        }
        System.out.println("--------------");

        for (int j=10; j>=0; j--) {
            System.out.println(j);
        }
    }
}
