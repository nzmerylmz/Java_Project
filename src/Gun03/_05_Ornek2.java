package Gun03;

public class _05_Ornek2 {
    public static void main(String[] args) {
        // Ornek soru: yarıcapı verilen bir
        // dairenin çevresini ve alanını bulunuz

        int r= 5;
        float Pi=3.14F;
        float cevre= 2*Pi*r;
        float alan= Pi*r*r;

        System.out.println("cevre = " + cevre);
        System.out.println("alan = " + alan);

    }
}
