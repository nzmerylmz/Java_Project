package Gun07;

public class _07_StringTrim {
    public static void main(String[] args) {
        // trim: stringin başındaki veya sonundaki boşluk karakterleri temizler

        String text=" Merhaba Dünya  ";
        System.out.println("textin orijinal hali = ->"+ text+"<-");
        System.out.println("textin trimli hali = ->" + text.trim()+"<-");
    }
}
